/*1. 📚 Online Library Management API


## Scenario

A library wants a REST API to manage books using a database.

## Requirements

Database Table: `books`

Base URL: `/books`

### APIs

* `POST /books/add` → Add a new book
* `GET /books` → Fetch all books
* `GET /books/{id}` → Fetch book by ID
* `PUT /books/update/{id}` → Update complete book details
* `PATCH /books/price/{id}` → Update only book price
* `DELETE /books/delete/{id}` → Delete book

## Model Fields

* id
* title
* author
* genre
* price

## Sample RequestBody

{
  "id": 101,
  "title": "Spring Boot",
  "author": "John",
  "genre": "Programming",
  "price": 550
}*/
