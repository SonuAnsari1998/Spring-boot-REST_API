/*8. ✈️ FlightBooking API

## Scenario

An airline company wants APIs to manage flight bookings.

## Requirements

Database Table: `flight_bookings`

Base URL: `/flights`

### APIs

* `POST /flights/book`
* `GET /flights`
* `GET /flights/{id}`
* `PUT /flights/update/{id}`
* `PATCH /flights/price/{id}`
* `DELETE /flights/delete/{id}`

## Model Fields

* bookingId
* passengerName
* flightName
* source
* destination
* ticketPrice

## Sample RequestBody

{
  "bookingId": 701,
  "passengerName": "Kiran",
  "flightName": "IndiGo",
  "source": "Hyderabad",
  "destination": "Delhi",
  "ticketPrice": 6500
}*/
